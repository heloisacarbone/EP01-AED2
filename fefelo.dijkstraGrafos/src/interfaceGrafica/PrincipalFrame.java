    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
 // TODO add your handling code here:
        
        int returnVal = jFileChooser2.showOpenDialog(this);
 
        String tipo = null;
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser2.getSelectedFile();
            

            try {
                                
                arq = file.getAbsolutePath();
                txtFinput.setText(arq);
                txtFinput.setSize(6,20);
                atxConteudo.read( new FileReader( file.getAbsolutePath() ), null );
            } catch (Exception e) {
                System.out.println("problem accessing file"+file.getAbsolutePath());
            }
        } else {
            System.out.println("File access cancelled by user.");
        }
        StringTokenizer values = new StringTokenizer(arq, ".");
        String extencao = null;
        while(values.hasMoreTokens()){
            extencao = values.nextToken();
        }
        if(extencao.equals("txt")){
            rbTxt.setSelected(true);
            rbXml.setSelected(false);
            lblExtensao.setText("txt");
            tipo = controllers.ControllerTipos.verificaTipoTxt(arq);
          }
        if(extencao.equals("xml")){
            rbXml.setSelected(true);
            rbTxt.setSelected(false);
            lblExtensao.setText("xml");
            tipo = controllers.ControllerTipos.verificaTipoXml(arq);
         }
        
        if(tipo.equals("1") || tipo.equals("2") || tipo.equals("3") ){
        lblTipo.setVisible(true);
        lblType.setText(tipo);
        lblType.setVisible(true);
        btnDijk.setEnabled(true);
        }
        else{
        lblType.setText("Arquivo inv√°lido");
        lblType.setVisible(true);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

